    private void descargarInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_descargarInputActionPerformed
        // TODO add your handling code here:
        try{
            int identificador = Integer.parseInt(this.descargarInputField.getText());
            ArrayList<Documento> lista = inv.getInventario();

            for (int i = 0; i < lista.size() ; i++){
                if (lista.get(i).getIdentificador() == identificador){
                    if (lista.get(i) instanceof Libro){
                        ((Libro)lista.get(i)).descargar();
                    }else if (lista.get(i) instanceof ArticuloRevista){
                        ((ArticuloRevista)lista.get(i)).descargar();
                    }else if (lista.get(i) instanceof Tesis){
                        JOptionPane.showMessageDialog(null, "Solo se pueden descargar libros Y revistas");
                    }else{
                        JOptionPane.showMessageDialog(null, "ingresa un número válido");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "No existe ningún documento con ese número idenificador.");
                    break;
                }

            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Por favor ingresa únicamente caracteres numéricos.");
        }
    }//GEN-LAST:event_descargarInputActionPerformed

