    private void aniadirBtnPanelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_aniadirBtnPanelMousePressed
        // TODO add your handling code here:
        setColor2(aniadirBtnPanel);
        try{
            String tipo=this.eleccion.getSelectedItem().toString();
            int identificador = Integer.parseInt(this.identificadorInput.getText());
            int anio = Integer.parseInt(this.anioDePublicacionInput.getText());
            int numPaginas = Integer.parseInt(this.numPaginasInput.getText());

            String titulo = this.tituloInput.getText();
            String autorNombre = this.nombreAutorInput.getText();
            String autorApellido = this.apellidoAutorInput.getText();
            String idioma = this.idiomaInput.getText();

            if(tipo.equals("Libro")){
                String lugarDePublicacion = this.lugarDePublicacionInput.getText();
                String editorial = this.editorialInput.getText();
                String genero = this.generoInput.getText();

                Libro l = new Libro(identificador,titulo,autorNombre,autorApellido,anio,numPaginas,idioma, lugarDePublicacion, editorial, genero);
                inv.aniadir(l);

            }else if(tipo.equals("Tesis")){
                String lugarDePublicacion = this.lugarDePublicacionInput.getText();
                String grado = this.gradoInput.getText();
                String universidad = this.universidadInput.getText();
                String campo = this.campoInput.getText();

                Tesis t = new Tesis(identificador,titulo,autorNombre,autorApellido,anio,numPaginas,idioma, lugarDePublicacion, grado, universidad, campo);
                inv.aniadir(t);

            }else if(tipo.equals("Revista")){
                String nombreRevista = this.nombreRevistaInput.getText();
                int volumen = Integer.parseInt(this.volumenInput.getText());
                int numeroRevista = Integer.parseInt(this.numeroRevistaInput.getText());

                ArticuloRevista r = new ArticuloRevista(identificador,titulo,autorNombre,autorApellido,anio,numPaginas,idioma, nombreRevista, volumen, numeroRevista);
                inv.aniadir(r);
            } else if(tipo.equals("Periodico")) {
                JOptionPane.showMessageDialog(null,"no disponible por el momento");
            } else {
                JOptionPane.showMessageDialog(null,"Error");
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Asegúrese de:\n1. Haber seleccionado un tipo específico"
                    + " de documento.\n2. Que todos los campos disponibles estén llenos.\n3. Que aquellos campos"
                    + " marcados con un asterisco reciban únicamente caracteres numéricos.");
        }

    }//GEN-LAST:event_aniadirBtnPanelMousePressed

